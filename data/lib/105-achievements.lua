function getAchievementInfoById(id)
	for k, v in pairs(achievements) do
		if k == id then
			local t = {}
			t.id = k
			for inf, it in pairs(v) do
				t[inf] = it
			end
			return t
		end
	end
	return false
end
 
function getAchievementInfoByName(name)
	for k, v in pairs(achievements) do
		if v.name:lower() == name:lower() then
			local t = {}
			t.id = k
			for inf, it in pairs(v) do
				t[inf] = it
			end
			return t
		end
	end
	return false
end
 
function getSecretAchievements()
	local t = {}
	for k, v in pairs(achievements) do
		if v.secret then
			table.insert(t, k)
		end
	end
	return t
end
 
function getPublicAchievements()
	local t = {}
	for k, v in ipairs(achivements) do
		if not v.secret then
			table.insert(t, k)
		end
	end
	return t
end
 
